#include <libft.h>

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	char *tmp_dst;
	char *tmp_src;

	if (n == 0 || dst == src)
		return (dst);
	tmp_dst = (char*)dst;
	tmp_src = (char*)src;
	while (n-- > 0)
		*tmp_dst++ = *tmp_src++;
	return (dst);
}
