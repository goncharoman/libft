#include <libft.h>

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char	*tmp;
	size_t	len;

	if (!s || !f)
		return (NULL);
	len = ft_strlen(s);
	if (!(tmp = ft_strnew(len)))
		return (NULL);
	while (len--)
		*(tmp + len) = f(len, *(s + len));
	return (tmp);
}
